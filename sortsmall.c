#include "push_swap.h"

void	sort2(t_stacks *s)
{
	if (s->a[0] > s->a[1])
		sa(s);
}

void	sort3(t_stacks *s)
{
	if (s->a[0] > s->a[1] && s->a[2] > s->a[0])
		sa(s);
	if (s->a[0] > s->a[1] && s->a[2] < s->a[1])
	{
		sa(s);
		rra(s);
	}
	else if (s->a[0] > s->a[1] && s->a[1] < s->a[2])
		ra(s);
	else if (s->a[0] < s->a[1] && s->a[1] > s->a[2] && s->a[0] < s->a[2])
	{
		sa(s);
		ra(s);
	}
	else if (s->a[0] < s->a[1] && s->a[1] > s->a[2])
		rra(s);
}
